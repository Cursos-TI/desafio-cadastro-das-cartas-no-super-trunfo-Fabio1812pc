#include <stdio.h>

int main () {
    printf("cidade1! \n");
    
    char estado;
    char codigo[6];
    char cidade[10];
    int população;
    float área;
    float PIB;
    int numeropontosturistico;

    printf("Digite o estado: \n");
    scanf("%s", &estado);

    printf("Digite o codigo: \n");
    scanf("%s", &codigo);

    printf("digite a cidade: \n");
    scanf("%s", &cidade);

    printf("digite o numero de população: \s");
    scanf("%d", &população);

    printf("Tamanho da área: \s");
    scanf("%f", &área);

    printf("Qual o PIB: \s");
    scanf("%f",&PIB);

    printf("Numero de pontos turistico: \s");
    scanf("%d", &numeropontosturistico);

    printf("nome do estado: %s - codigo: %s - cidade:%s", estado, codigo, cidade);
    printf("população: %d - área: %f - PIB: %f", população, área, PIB);
    printf("numero pontos turistico: %d");

    return 0;

}

